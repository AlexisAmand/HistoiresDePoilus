        <h2>La Bataille de la Marne</h2>

        <p class="text-justify"><em>"L'Armée Française prend l'offensive, le 294e Régiment d'Infanterie prend par en 1re ligne au combat d'Etrépilly et attaque la ferme de Champfleury, dont il s'empare le 5 septembre. Le 6 et le 7, les combats se succèdent très violents et le Régiment poursuivant l'ennemi continue sa progression vers l'Aisne."</em></p>

        <figure class="text-center">
