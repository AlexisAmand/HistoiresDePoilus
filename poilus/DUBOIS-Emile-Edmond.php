        <h2>La mobilisation d'août 1914</h2>

        <p class="text-justify">Âgé de 34 ans au moment de <a href="mobilisation-1914.php" title="Voir le décret de mobilisation générale de 1914">la mobilisation</a>, il intègre le <strong>94e Régiment d'Infanterie</strong> qui donnera naissance au <strong>294e Régiment d'Infanterie</strong>. En effet, à la mobilisation, chaque régiment d'active crée un régiment de réserve dont le numéro est le sien plus 200.  Je pense que c'est ce qui explique que sa fiche matricule indique qu'il fait parti du 94e alors que sa fiche de mort pour la France indique le 294e.</p>

        <figure class="text-center">
            <a href="sources/DUBOIS-Emile-Edmond-matricule.jpg" data-fancybox="gallery" data-caption="Extrait de la fiche matricule d'Émile DUBOIS">
            <img src="miniatures/DUBOIS-Emile-Edmond-matricule.jpg" class="figure-img img-fluid img-thumbnail" alt="Extrait de la fiche matricule d'Émile DUBOIS"/></a>
            <figcaption class="figure-caption">Extrait de sa fiche matricule<br />(source: Archives Départementales du Nord)</figcaption> 
        </figure>

        <p class="text-justify">La partie qui suit est directement issue de l'Historique du 294e Régiment d'Infanterie, j'ai juste apporté 2 ou 3 petites modifications pour faciliter la lecture.</p>

        <p class="text-justify"><em>"Formé à deux bataillons, <strong>Émile Edmond DUBOIS</strong> et le 294e Régiment d'Infanterie quittent Bar-le-Duc et entrent en campagne le 9 août sous les ordres du Lieutenant Colonel DUPERRIER. Le 294e R.I fait partie de la 56e Division d'Infanterie, 111e Brigade. Au cours de la campagne de 1914, il participe au combat de Buzy, de Senlis, à la Bataille de la Marne, au combat sur l'Aisne et à la Bataille de l'Oise."</em></p>

        <h2>Le combat de Buzy</h2>

        <p class="text-justify"><em>"Le choc a lieu le 25 août. Le Régiment est en deuxième ligne. La 56e Division d'Infanterie obtient un succès important et repousse complètement l'ennemi à Buzy le contraignant à battre en retraite et lui infligeant de grosses pertes. La situation critique de l'Aile Gauche de l'Armée Française est telle que le succès n'est pas exploité et la 56e Division d'Infanterie reçoit l'ordre de rompre le combat pour être transportée vers le Nord. Le 294e Régiment d'Infanterie embarque en chemin de fer à Dugny et à Dieue, débarque à Moyenneville et Tricot (Oise) et se dirige sur Montdidier pour prendre position en avant de Senlis."</em></p>

        <div id="Duperrier_Selva"></div>

        <h2>Le combat de Senlis</h2>

        <p class="text-justify"><em>"La 56e Division d'Infanterie a pour mission de ralentir la marche de l'ennemi dont les avant-gardes cherchent à se rapprocher de Paris. Le 2 septembre, le régiment en arrière garde de la 56e Division d'Infanterie, reçoit l'ordre de tenir les lisères de Senlis jusqu'à 13 heures. Dès l'aube, les éléments avancés de l'ennemi attaquent, mais ne parviennent pas, malgré le tir acharné de leur artillerie, à nous déloger et le Régiment résiste sur les positions qui ont été assignées jusqu'à 15 heures, se replie ensuite par les bois et par la ville de Senlis. Le 3, le Régiment se dégage et se replie sur Ory-la-Ville, le Lieutenant Colonel DUPERRIER, malade, est évacué et le Chef de Bataillon SELVA prend le commandement du régiment. Le 4, le Régiment cantonne à Dammartin-en-Goele, et prend le lendemain la direction de l'Est, se dirigeant vers Saint-Soupplet."</em></p>

