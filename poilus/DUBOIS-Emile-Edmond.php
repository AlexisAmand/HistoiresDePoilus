    <h1 class="my-4">Titre de la page</h1>

    <p class="text-justify"><strong>Émile Edmond DUBOIS</strong>, le beau frère de <a href="GRAIN-Louis.php" title="biographie du soldat Louis GRAIN">Louis GRAIN</a> et de <a href="GRAIN-Leon-Albert.php" title="biographie du poilu Léon GRAIN">Léon GRAIN</a>, est né le 25 janvier 1880 à Villers-Guislain (Nord). Il est le fils légitime de Désiré Émile et Catherine Vénérose BROYE. En 1906, il est tisseur et vit à Villers-Guislain (Rue Hérode) avec ses parents, sa sœur Malvina et son frère Christophe. Le 3 décembre 1907, il épouse Fortunée Luce GRAIN à Villers-Guislain.</p>

