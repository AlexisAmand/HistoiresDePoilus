        <h2>Émile, mort pour la France</h2>

        <p class="text-justify"><strong>Émile Edmond DUBOIS </strong> est porté disparu au nord de <a href="http://cartespostalesanciennes.genealexis.fr/album/87-souain" title="cartes postales ancienne de Souain sur le site Genealexis">Souain</a> (Marne, Souain-Perthes-lès-Hurlus depuis le 16 juin 1950) le 8 octobre 1915 à l'âge de 35 ans. Son décès a été fixé à cette date par jugement du tribunal de Dijon le 18 mai 1920. Son nom figure sur le Monument aux Morts du village de Villers-Guislain (à côté de Cambrai).</p><p class="text-justify">&nbsp;</p>

