        <h2>Le combat sur l'Aisne</h2>

        <p class="text-justify">Le 13 septembre, Émile DUBOIS et son régiment reçoivent l'ordre de traverser l'Aisne. Ils se heurtent à Saconin-et-Breuil, à une résistance acharnée de l'ennemi dont l'artillerie déclenche des barrages continuels sur les ponts (Combat de Vaux-Mercin). Après plusieurs tentatives meurtrières, le Régiment parvient le 15 à franchir la rivière après avoir glissé sur la droite jusque 4 km environ à l'Est de Vic-sur-Aisne, à Fontenoy. Le Régiment se regroupe sur la rive droite et prend part aux combats livrés sur le plateau de Nouvron les 17-18-19 et 20 septembre (Combat de Berry-au-Bac). Placé en réserve le 21, Émile et le 294e Régiment d'Infanterie prennent la direction au Nord et après avoir traversé Carlepont et Ribécourt, et sont embarqués à Compiègne à destination de la Boissière – Fécamp ou s'opère le débarquement.</p>

        <h2>La Bataille de l'Oise</h2>

        <p class="text-justify">L'historique du 294e R.I continue :<em> "Le repli ennemi s'est arrêté ; les Allemands sont solidement organisés au Sud de Roye. Le Régiment en première ligne vient s'installer aussitôt débarqué à cheval sur la voie ferrée Roye – Compiègne à la hauteur du passage à niveau de Beuvraigne et de l'église de ce village où nos patrouilles se sont heurtées aux avants postes ennemis."</em></p> 

        <figure class="text-center">
            <a href="images/Beuvraignes-1914.jpg" data-fancybox="gallery" data-caption="Beuvraignes pendant la Grande Guuerre">
            <img src="miniatures/Beuvraignes-1914.jpg" class="figure-img img-fluid img-thumbnail"  alt="Le village de Beuvraignes pendant la Première Guerre Mondiale"/></a>
