    </a>
    <figcaption>Arbre généalogique de la famille CARIN<br />(cliquez pour agrandir)</figcaption>
</figure>

<figure>
    <a href="sources/CARIN-matricule-03.jpg" id="single_image" title="Théophile CARIN - Extrait de sa fiche matricule">
