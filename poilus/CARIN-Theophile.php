<figure>
    <a href="images/camp-Tatahouine.jpg" id="single_image" title="Tatahouine (Tunisie) - Le camp retranché">
    <img src="miniatures/camp-Tatahouine.jpg" alt="Le camp retranché de Tatahouine pendant la campagne de 1915-1916"/>
    </a>
