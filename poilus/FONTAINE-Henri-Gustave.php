        <h2>La mobilisation d'août 1914</h2>

        <p class="text-justify">Âgé de 24 ans au début du conflit, <strong>Henri Gustave</strong> devient soldat de 2e classe au sein du <strong>91e Régiment d'Infanterie</strong>. Il est réformé le 18 août 1914 pour emphysème pulmonaire. Il s'agit d'une maladie souvent liée au tabac qui touche les alvéoles pulmonaires et détruit progressivement les poumons.</p>

        <h2>Anzin, ville occupée</h2>

        <p class="text-justify">Anzin, commune où est né <strong>Henri</strong>, est occupée par l'armée allemande dès le 25 août 1914, aussitôt un couvre-feu instauré.</p>

        <figure class="text-center">
            <a href="images/anzin-1914-01.jpg"  data-fancybox="gallery" data-caption="La Croix d'Anzin pendant la Grande Guerre (1914-1918)">
            <img src="miniatures/anzin-1914-01.jpg" class="figure-img img-fluid img-thumbnail" alt="La Croix D'Anzin en pendant la Première Guerre Mondiale (1914-1918) sur une carte postale ancienne"/></a>
            <figcaption class="figure-caption">La Croix d'Anzin détruite en 1914-18<br />(Source: Bibliothèque Municipale de Valenciennes)</figcaption>
        </figure>

        <p class="text-justify"><a href="http://cartespostalesanciennes.genealexis.fr/album/36-anzin" title="cartes postales anciennes d'Anzin sur le site Genealexis">Anzin</a> avait la particularité d'être située dans la « zone d'étape », qui comprenait toutes les compagnies du Nord, la compagnie des mines d'Anzin reprit l'exploitation au bout de quelques semaines en partie avec du personnel réquisitionné et sous le contrôle étroit de la Bergverwaltung (section des mines de l'administration allemande). Sa production est toutefois  inférieure de 30 % à celle de 1913. </p>

        <figure class="text-center">
            <a href="images/anzin-rue-lecaillez-1914-1918.jpg"  data-fancybox="gallery" data-caption="Les ruines de la rue Lecaillez à Anzin sur une carte postale ancienne">
