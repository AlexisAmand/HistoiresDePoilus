    <h1 class="my-4">Henri Gustave FONTAINE</h1>

    <p class="text-justify"><strong>Henri Gustave FONTAINE</strong>, le père de mon arrière-grand-mère, est né le 24 février 1890 à Anzin (Nord). Il est le fils légitime de Henri François FONTAINE, ouvrier mineur, âgé de 32 ans et de Désirée BAR, âgée de 26 ans. En 1906, Henri est journalier. Il vit Rue Kléber à Anzin avec ses parents et ses frères et sœurs: Virginie, Désirée, Gustave, Marie et Léonie.</p>

        <figure class="text-center">
            <a href="images/anzin-rue-kleber.jpg" data-fancybox="gallery" data-caption="La rue Kléber à Anzin">
            <img src="miniatures/anzin-rue-kleber.jpg" class="figure-img img-fluid img-thumbnail" alt="La rue Kléber à Anzin sur une carte postale"/></a>
            <figcaption class="figure-caption">La Rue Kléber à Anzin<br />(Source: Bibliothèque municipale de Valenciennes)</figcaption>
        </figure>

        <p class="text-justify">Il s'unit à Valenciennes (Nord) avec Bertha CANONNE le 3 avril 1914. Bertha est la fille légitime de Nestor CANONNE et de Marie Eugénie DUCHÊNE. Le couple aura plusieurs enfants: Henri, Madeleine (mon arrière-grand-mère, qui épousera Kleber le fils de <a href="FIEVET-Charles-Augustin.php" title="Biographie du soldat Charles Augustin FIEVET">Charles Augustin FIEVET</a>), René, Marcel et Marie-Louise.</p>

        <figure class="text-center">
