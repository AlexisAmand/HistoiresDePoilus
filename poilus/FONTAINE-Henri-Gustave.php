            <figcaption class="figure-caption">La rue Lecaillez<br />(Source: Bibliothèque Municipale de Valenciennes)</figcaption>
        </figure>

        <p class="text-justify">L'exploitation industrielle fut par contre arrêtée : les usines de la Société des Forges furent totalement démolies, les machines et ferrailles étant récupérées au profit de l'industrie allemande ; l'usine Escaut-et-Meuse fut transformée en atelier de réparation de canons. </p><p class="text-justify">La ville d'Anzin est libérée le 1er novembre 1918.</p>

