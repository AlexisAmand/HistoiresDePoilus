        <h2>Louis, prisonnier au camp de Münster</h2>

        <p class="text-justify">Le <strong><a href="../camps/camp-munster<p class="text-justify">camp de Münster</a></strong>, où est envoyé Louis, est ouvert dès le début de la guerre, il était situé en Allemagne, dans le nord du Land de Rhénanie-du-Nord-Westphalie.</p>

