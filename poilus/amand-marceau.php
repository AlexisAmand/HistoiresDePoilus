        <h1>Marceau AMAND</h1>
        
        <p>Marceau AMAND est né le 17 avril 1899 à Aubervilliers (département de la Seine, aujourd'hui la Seine-Saint-Denis) fils légitime de Blomy et Adeline DELPLANQUE. En 1919, il vit à Neuville-sur-Escaut, une ville du Nord de la France d'où sont originaires ses parents. Marceau est le frère de <a href="amand-hoche.php" title="biographie du poilu Hoche Gaston AMAND">Hoche Gaston Blomy</a>. Selon son acte de naissance, il est né à bord du bateau de ses parents "La Déroute" sur les bords du canal. Je ne suis pas parents avec Marceau, il est le grand-oncle d'Alain AMAND, un pianiste dont j'ai commencé à étudier <a href="http://www.genealexis.fr/genealogie/alain_amand/heredis/accueil.htm" title="Généalogie du pianiste Alain AMAND">la généalogie</a>.</p>
        
        <figure>
            <a href="sources/amand-marceau-1899.jpg" data-fancybox="gallery" data-caption="Acte de naissance de Marceau (1899)">
            <img src="miniatures/amand-marceau-1899.jpg" alt="Acte de naissance de Marceau AMAND"/></a>
            <figcaption>Acte de naissance de Marceau (1899)<br />(Source: Archives Départementales de la Seine-Saint-Denis)</figcaption>
        </figure>
        
        <p>Il est rattaché à la classe de mobilisation 1915 car il n'a pas pu être recensé en temps utile par suite d'un cas de force majeure (sans doute la guerre). il est finalement appelé au service armé en 1919 et affecté au 10e Régiment de Hussards où il est incorporé à compté du 12 juin. Soldat de 2e classe, il passe au 2e Régiment de Hussards le 10 novembre 1919. Il reste au sein de "l'escadron de la motte" de ce régiment jusqu'au 25 mars 1921, date à laquelle il est renvoyé dans ses foyers en attendant son passage dans la réserve de l'armée active avec le certificat de bonne conduite qui lui est accordé. Un escadron est une unité militaire qui marque l'appartenance à une unité armée dite "à cheval", c'est l'équivalent des compagnies des unités de l'armée de terre dite "à pied".</p>

        <figure>
            <a href="images/hussards.jpg" data-fancybox="gallery" data-caption="Un hussard en uniforme vers 1914">
            <img src="miniatures/hussards.jpg" alt="Dessin d'un hussard au début de la Première Guerre Mondiale"/></a>
            <figcaption>Un hussard en uniforme vers 1914<br />(Dessin original de Maurice TOUSSAINT)</figcaption>
        </figure>
        
        <p>Son service militaire inclut une période de 4 mois (du 12 juin au 23 octobre 1919) où il est considéré comme en campagne contre l'Allemagne. En effet, selon la loi publiée dans le Journal Officiel de la République Française du 24 octobre 1919, la fin de la guerre est fixée au 23 octobre de la même année et de la même façon la mobilisation générale prend fin par le décret publié dans le JO du 15 octobre 1919.</p>
       	
        <figure>
            <a href="sources/matricule-marceau-amand.jpg" data-fancybox="gallery" data-caption="Les domiciles de Marceau AMAND">
            <img src="miniatures/matricule-marceau-amand.jpg" alt="Extrait de la fiche matricule de Marceau AMAND contenant la liste de ses domiciles"/></a>
            <figcaption>Ses domiciles sur sa fiche matricule<br />(Source: Archives Départementales de la Seine-Saint-Denis)</figcaption>
        </figure>
              
        <p>Selon la mention en marge de son acte de naissance, il est décédé à Beaucaire, sur la rive du Rhône, à côté de Nîmes, le 19 janvier 1969. Il repose au cimetière de Neuville-sur-Escaut en compagnie de Kleber AMAND (1902-1987), Celestine OCCULISSE (1874-1957), Amélie POTIER (1907-1989) et Denis POTIER (1869-1943).</p>
        
