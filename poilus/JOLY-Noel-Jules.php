        <h2> Noël et le "Centre de résistance de Rocq"</h2>

        <p class="text-justify">Selon son dossier d'ancien combattant, <strong>Noël Jules</strong> fait partie de la 16e batterie du 13e groupe auxiliaire du <strong>1er Régiment d'Artillerie à Pied</strong> sous les ordres du capitaine LAFEUILLE (voir document en bas de la page). Pourquoi une telle différence entre la fiche matricule, qui indique le 2e R.I.T, et sa carte d'ancien combattant qui indique 1er R.A.P ? Mystère...!</p><p class="text-justify">Les soldats du 1er Régiment d'Artillerie à Pied étaient stationnés sur l'ensemble qui prendra le nom de « Centre de résistance de Rocq ». </p><p class="text-justify">Le 2 septembre 1914, le bombardement reprend sur les ouvrages du secteur attaqué : Bersillies, la Salmagne, le Fagnet et Boussois. Les ouvrages de Rocq et de Cerfontaine continuent à être bombardés à une cadence régulière. Aux batteries de Rocq, artilleurs des 1e et 3e régiments à pied et fantassins du 1er  territorial opposent une belle résistance. </p>

        <figure class="text-center">
            <a href="images/fort-cerfontaine.jpg" data-fancybox="gallery" data-caption="L'entrée du Fort de Cerfontaine">
            <img src="miniatures/fort-cerfontaine.jpg" class="figure-img img-fluid img-thumbnail" alt="L'entrée du Fort de Cerfontaine"/></a>
            <figcaption class="figure-caption">Le Fort de Cerfontaine<br />(source: http://www.mapio.cz/ )</figcaption>
        </figure>    

        <p class="text-justify">Le 3 septembre, un déluge d'obus allemands, de gros calibres, du 210 et du 420, tombe pendant 2 jours sur les secteurs de Fagnet, la Salmagne, Boussois, Cerfontaine et Rocq où est cantonné Noël JOLY. </p><p class="text-justify">Le <strong>Centre de résistance de Rocq</strong> résistera héroïquement, plus d'une semaine, jusqu'au 6 septembre 1914, date à laquelle le secteur nord-est donne des signes de craquement. Le matin, au sud-est, une attaque est repoussée devant la position de Rocq. Au nord-est, deux attaques échouent mais une troisième parvient à enlever l'ouvrage de la Salmagne. Celui de Bersillies succombe à son tour le soir.</p>

        <figure class="text-center">
