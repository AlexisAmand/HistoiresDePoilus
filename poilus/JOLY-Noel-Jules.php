    <h1 class="my-4">Noël Jules JOLY</h1>

    <p class="text-justify"><strong>Noël Jules JOLY</strong>, père de mon arrière-grand-père, est né le 26 décembre 1873 à <a href="http://genealexis.fr/cartes-postales/quarouble.php" title="Histoire de Quarouble">Quarouble</a> (Nord). Il est le fils légitime de Jules JOLY, âgé de 32 ans, et de Cressance Appoline DUEE, âgé de 22 ans. En 1906, Noël est mouleur en creux à la faïencerie d'Onnaing où il vit, rue basse, en compagnie de son épouse Adélaïde PELEZ, qu'il a épousé à Quarouble le 9 décembre 1895, et de ses enfants Alfred, Louise et Noël (mon arrière-grand-père).</p>

        <figure class="text-center">
            <a href="images/arbre-joly.png" data-fancybox="gallery" data-caption="Arbre généalogique simplifié de la famille JOLY">
            <img src="miniatures/arbre-joly.png" class="figure-img img-fluid img-thumbnail" alt="Arbre généalogique simplifié de la famille JOLY" class="ArbreGenealogique"/></a>
            <figcaption class="figure-caption">Arbre simplifié de la famille JOLY<br />(cliquez pour agrandir)</figcaption>
        </figure>

        <p class="text-justify">Pour la petite histoire, Lucia CARLIER (fille de <a href="CARLIER-Druon-Joseph.php" title="lien vers la page consacrée à Druon Carlier">Druon CARLIER</a>) épouse Noël JOLY (le fils de <a href="JOLY-Noel-Jules.php" title=" ">Noël Jules JOLY) </a>le 24 septembre 1927 à Onnaing. </p>

        <figure class="text-center">
            <a href="portraits/joly-noel-jules.jpg" data-fancybox="gallery" data-caption="Portrait de Noël Jules JOLY">
