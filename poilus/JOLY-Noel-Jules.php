        <h2>La mobilisation d'août 1914</h2>

        <p class="text-justify">Selon sa fiche matricule, <strong>Noël Jules</strong> devient soldat 2e classe au sein du <strong>2e Régiment d'Infanterie Territoriale</strong>. En 1914, Les régiments d'infanterie territoriale (R.I.T), sont essentiellement constitués de citoyens anciens nés entre 1875 et 1880. Ils sont surtout assignés à des missions plus statiques, moins exposées: gares, nœuds ferroviaires, ponts, entrepôts, places fortes, etc. Noël, quant à lui, cantonne à  Ferrière.</p>

        <figure class="text-center">
            <a href="sources/Joly-noel-matricule-01.jpg" data-fancybox="gallery" data-caption="Noël Jules JOLY, extrait de sa fiche matricule.">
