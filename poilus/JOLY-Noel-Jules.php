        </figure>   

        <h2>La chute de Maubeuge</h2>

        <p class="text-justify">Le Gouverneur fait rassembler les drapeaux de la garnison le 4 septembre. Ils sont au nombre de neuf, ce sont ceux des 145e  et 345e  d'infanterie, des 31e  et 32e  colonial, des 1e , 2e , 3e  et 4e  territorial et enfin celui du 1er régiment d'artillerie à pied. Le lendemain, à 7 heures du matin, les drapeaux de la garnison sont brûlés dans le jardin du Pavillon, près de la Caserne Joyeuse, en présence d'un piquet de marsouins qui rend les honneurs. Le capitaine Prévost commande la cérémonie qui émotionne fortement les assistants.</p>

        <figure class="text-center">
