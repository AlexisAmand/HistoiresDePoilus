        <h2>Prisonnier en Allemagne</h2>

        <p class="text-justify"><strong>Noël Jules</strong> est fait prisonnier le 7 septembre. Les troupes françaises de Maubeuge capturées à Maubeuge sont ensuite rassemblées à Rousies et une partie (dont <strong>Noël Jules JOLY</strong>) est envoyée en internement  à <a href="../camps/camp-friedrichsfeld.php" title=" ">Friedrichsfeld</a> (Allemagne) et <a href="../camps/camp-munster.php" title="le camp de prisonniers de Munster en Allemagne">Munster III</a>. <strong>Noël</strong> sera  interné à Munster III du 7 septembre 1914 au 18 décembre 1918.</p>

        <figure class="text-center">
            <a href="images/friedrichsfeld-baraque.jpg" data-fancybox="gallery" data-caption="Une baraque à l'intérieur de camp de prisonniers de Friedrichsfeld">
            <img src="miniatures/friedrichsfeld-baraque.jpg" class="figure-img img-fluid img-thumbnail" alt="Le camp de prisonniers de Friedrichsfeld sur une carte postale ancienne"/></a>
            <figcaption class="figure-caption">Une baraque à Friedrichsfeld<br />(source: Delcampe)</figcaption>
        </figure>

        <p class="text-justify"><strong>Noël Jules</strong> est libéré le 18 décembre 1918 et envoyé en congé illimité le 11 février 1919 par le 1er d'artillerie. Il se retire à Onnaing, rue Émile Zola.</p>

        <h2>L'après guerre de Noël</h2>

        <p class="text-justify">Le 9 septembre 1929, <strong>Noël Jules</strong> se voit délivrer une carte d'ancien combattant par le comité départemental. Il habite alors 30 rue Voltaire à Onnaing.</p>

        <figure class="text-center">
            <a href="sources/joly-noel-jules-carte.jpg" data-fancybox="gallery" data-caption="Carte d'ancien combattant de Noël Jules JOLY">
            <img src="miniatures/joly-noel-jules-carte.jpg" class="figure-img img-fluid img-thumbnail" alt="Carte d'ancien combattant de Noël Jules JOLY"/></a>
            <figcaption class="figure-caption">Carte d'ancien combattant ayant appartenu à Noël<br />(source: Archives Départementales du Nord)</figcaption> 
        </figure>

        <p class="text-justify">Cependant le 3 décembre 1947,<strong> Noël Jules</strong> apprend qu'il n'a plus droit à la carte du combattant au titre de l'<a href="jorf-3-juillet-1930.php" title="lien vers le décrêt du 1er juillet 1930">article 2 du décret du 1er juillet 1930</a>. En effet, il était présent au <strong>1er Régiment d'Artillerie à Pied</strong> comme auxiliaire d'artillerie de places fortes et il s'agissait d'un « emploi non combattant ». Il faut bien comprendre que les régiments de territoriaux constitués par des hommes de 43 à 49 ans n'avaient pas pour vocation principale de participer à des combats mais plutôt d'occuper toutes les tâches non combattantes.</p>

