<h1>JOLY Noël Jules</h1>

<p><strong>Noël Jules JOLY</strong>, père de mon arrière-grand-père, est né le 26 décembre 1873 à <a href="http://genealexis.fr/cartes-postales/quarouble.php" title="Histoire de Quarouble">Quarouble</a> (Nord). Il est le fils légitime de Jules JOLY, âgé de 32 ans, et de Cressance Appoline DUEE, âgé de 22 ans. En 1906, Noël est mouleur en creux à la faïencerie d&#39;Onnaing où il vit, rue basse, en compagnie de son épouse Adélaïde PELEZ, qu&#39;il a épousé à Quarouble le 9 décembre 1895, et de ses enfants Alfred, Louise et Noël (mon arrière-grand-père).</p>
    <p>Pour la petite histoire, Lucia CARLIER (fille de <a href="CARLIER-Druon-Joseph.php" title="lien vers la page consacrée à Druon Carlier">Druon CARLIER</a>) épouse Noël JOLY (le fils de <a href="JOLY-Noel-Jules.php">Noel Jules JOLY) </a>le 24 septembre 1927 à Onnaing. </p>
  
<figure>
    <a href="portraits/joly-noel-jules.jpg" id="single_image" title="Portrait de Noël Jules JOLY">
    <img src="miniatures/joly-noel-jules.jpg" alt="Portrait de Noël Jules JOLY"/></a>
    <figcaption>Portrait de Noël Jules<br />(cliquez pour agrandir)</figcaption>
</figure>

<br/><h4>La mobilisation d'août 1914</h4>

<p>Selon sa fiche matricule, <strong>Noël Jules</strong> devient soldat 2e classe au sein du <strong>2e Regiment d’Infanterie Territoriale</strong>. En 1914, Les régiments d’infanterie territoriale (R.I.T), sont essentiellement constitués de citoyens anciens nés entre 1875 et 1880. Ils sont surtout assignés à des missions plus statiques, moins exposées: gares, nœuds ferroviaires, ponts, entrepôts, places fortes, etc. Noël, quant à lui, cantonne à  Ferrière.</p>

<figure>
    <a href="sources/Joly-noel-matricule-01.jpg" id="single_image" title="Noël Jules JOLY, extrait de sa fiche matricule.">
    <img src="miniatures/Joly-noel-matricule-01.jpg" alt="extrait de la fiche matricule de Noël Jules JOLY"/></a>
    <figcaption>Extrait de sa fiche matricule<br />(cliquez pour agrandir)</figcaption>
</figure>

<br/><h4> Noël et le &quot;Centre de résistance de Rocq&quot;</h4>

<p>Selon son dossier d’ancien combattant, <strong>Noël Jules</strong> fait partie de la 16e batterie du 13e groupe auxiliaire du <strong>1er Regiment d’Artillerie à Pied</strong> sous les ordres du capitaine Lafeuille (voir document en bas de la page). Pourquoi une telle différence entre la fiche matricule, qui indique le 2e R.I.T, et sa carte d'ancien combattant qui indique 1er R.A.P ? Mystère...!</p><p>Les soldats du 1er Regiment d’Artillerie à Pied étaient stationnés sur l’ensemble qui prendra le nom de « Centre de résistance de Rocq ». </p><p>Le 2 septembre 1914, le bombardement reprend sur les ouvrages du secteur attaqué : Bersillies, la Salemagne, le Fagnet et Boussois. Les ouvrages de Rocq et de Cerfontaine continuent à être bombardés à une cadence régulière. Aux batteries de Rocq, artilleurs des 1e et 3e régiments à pied et fantassins du 1er  territorial opposent une belle résistance. </p>
    
<figure>
    <a href="images/fort-cerfontaine.jpg" id="single_image" title="L'entrée du Fort de Cerfontaine">
    <img src="miniatures/fort-cerfontaine.jpg" alt="L'entrée du Fort de Cerfontaine"/></a>
    <figcaption>Le Fort de Cerfontaine<br />(cliquez pour agrandir - source: http://www.mapio.cz/ )</figcaption>
</figure>    
    
    <p>Le 3 septembre, un déluge d’obus allemands, de gros calibres, du 210 et du 420, tombe pendant 2 jours sur les secteurs de Fagnet, la Salmagne, Boussois, Cerfontaine et Rocq où est cantonné Noël JOLY. </p><p>Le <strong>Centre de résistance de Rocq</strong> résistera héroïquement, plus d’une semaine, jusqu’au 6 septembre 1914, date à laquelle le secteur nord-est donne des signes de craquement. Le matin, au sud-est, une attaque est repoussée devant la position de Rocq. Au nord-est, deux attaques échouent mais une troisième parvient à enlever l’ouvrage de la Salmagne. Celui de Bersillies succombe à son tour le soir.</p>

<figure>
    <a href="images/forts-maubeuge.jpg" id="single_image" title="L'emplacement des forts de la place forte de Maubeuge en 1914">
    <img src="images/mini/forts-maubeuge.jpg" alt="carte de l'emplacement des forts de la place forte de Maubeuge et la position des troupes le 3 septembre 1914"  /> 
    </a>
    <figcaption>L'emplacement des forts de la place forte de Maubeuge<br />et position des troupes le 3 septembre 1914<br />(Source : Das Ehrenbuch der Deutschen Schweren Artillerie - Tome 1)</figcaption>
</figure>    

<br/><h4>La chute de Maubeuge</h4>

<p>Le Gouverneur fait rassembler les drapeaux de la garnison le 4 septembre. Ils sont au nombre de neuf, ce sont ceux des 145e  et 345e  d'infanterie, des 31e  et 32e  colonial, des 1e , 2e , 3e  et 4e  territorial et enfin celui du 1er régiment d'artillerie à pied. Le lendemain, a 7 heures du matin, les drapeaux de la garnison sont brûlés dans le jardin du Pavillon, près de la Caserne Joyeuse, en présence d'un piquet de marsouins qui rend les honneurs. Le capitaine Prévost commande la cérémonie qui émotionne fortement les assistants.</p>

<figure>
    <a href="images/Maubeuge-caserne-joyeuse.jpg" id="single_image" title="La caserne Joyeuse (Maubeuge) avant 1914">
    <img src="miniatures/Maubeuge-Caserne-joyeuse.jpg"  alt="La caserne Joyeuse de Maubeuge avant la Première Guerre Mondiale"/></a>
    <figcaption>La Caserne Joyeuse à Maubeuge<br />(cliquez pour agrandir)</figcaption>
</figure>

<br/><h4>Prisonnier au camp de Friedrichsfeld</h4>

<p><strong>Noël Jules</strong> est fait prisonnier le 7 septembre. Les troupes françaises de Maubeuge capturées à Maubeuge sont ensuite rassemblées à Rousies et une partie (dont <strong>Noël Jules JOLY</strong>) est envoyée en internement  à <a href="../camps/camp-friedrichsfeld.php">Friedrichsfeld</a> (Allemagne) et <a href="../camps/camp-munster.php">Munster III</a>. <strong>Noël</strong> sera  interné du 7 septembre 1914 au 18 décembre 1918.</p>

<figure>
    <a href="images/friedrichsfeld-baraque.jpg" id="single_image" title="Une baraque à l'intérieur de camp de prisonniers de Friedrichsfeld">
    <img src="miniatures/friedrichsfeld-baraque.jpg" alt="Le camp de prisonniers de Friedrichsfeld sur une carte postale ancienne"/></a>
    <figcaption>Une baraque à Friedrichsfeld<br />(cliquez pour agrandir)</figcaption>
</figure>

    <p><strong>Noël Jules</strong> est libéré le 18 décembre 1918 et envoyé en congé illimité le 11 février 1919 par le 1er d&#39;artillerie. Il se retire à Onnaing, rue Emile Zola.</p>

<br /><h4>L'après guerre de Noël</h4>

<p>Le 9 septembre 1929, <strong>Noël Jules</strong> se voit délivrer une carte d'ancien combattant par le comité départemental. Il habite alors 30 rue Voltaire à Onnaing.</p>

<figure>
    <a href="sources/joly-noel-jules-carte.jpg" id="single_image" title="Carte d'ancien combattant de Noël Jules JOLY">
    <img src="miniatures/joly-noel-jules-carte.jpg" alt="Carte d'ancien combattant de Noël Jules JOLY"/></a>
    <figcaption>Carte d'ancien combattant ayant appartenu à Noël<br />(cliquez pour agrandir)</figcaption>
</figure>

<br/><p>Cependant le 3 décembre 1947,<strong> Noël Jules</strong> apprend qu’il n’a plus droit à la carte du combattant au titre de l’<a href="sources/jorf-3-juillet-1930.php" title="lien vers le décrêt du 1er juillet 1930">article 2 du décret du 1er juillet 1930</a>. En effet, il était présent au <strong>1er Regiment d’Artillerie à Pied</strong> comme auxilliaire d’artillerie de places fortes et il s’agissait d’un « emploi non combattant ». Il faut bien comprendre que les régiments de territoriaux constitués par des hommes de 43 à 49 ans n&#39;avaient pas pour vocation principale de participer à des combats mais plutôt d&#39;occuper toutes les tâches non combattantes.</p>

