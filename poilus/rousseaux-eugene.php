<h1>ROUSSEAU Eugène Fernand</h1>

<p><em>Cette page est en cours de redaction</em></p>

<p>Né à Lille le 19 juillet 1891, Eugène Fernand ROUSSEAUX est le frère de <a href="ROUSSEAUX-Louis.php">Louis Henri ROUSSEAUX</a> ainsi que le cousin de <a href="FIEVET-Auguste-Victor.php">FIEVET Auguste Victor</a> et <a href="FIEVET-Charles-Augustin.php">FIEVET Charles Augustin</a>.</p>

<p>Dans le cadre du service militaire, Eugène est incorporé à compter du 10 octobre 1912 en tant que soldat de 2e classe au 127e R.I. Il est soldat de 1er classe dès de 11 juillet 1913. Eugène est fait prisonnier de guerre à Vandegies (Vendegies-sur-Ecaillon ou Vendegies-au-Bois ?) le 25 aout 1914, et envoyé en captivité à Rammerstein et à <a href="../camps/camp-minden.php" title="le camp de prisonnier de Minden">Minden</a>. Il passe dans la Réserve de l'armée active le 1er octobre 1914 et est rapatrié le 28 décembre 1918. (source: fiche matricule)</p>

<p>Le 27 février 1915, il est sur la liste des prisonniers du camp de Senne.</p>

<figure>
    <a href="sources/rousseau-cicr-02.JPG" id="single_image" title="Liste de prisonniers du camp de Senne">
    <img src="miniatures/rousseau-cicr-02.JPG" alt="Extrait de la liste de prisonnier du camp de Senne"/>
    </a>
    <br />Liste de prisonniers du camp de Senne<br />(cliquez pour agrandir)
</figure>

<p>En Mai 1916, Il arrive au camp de <a href="../camps/camp-minden.php" title="le camp de prisonnier de Minden">Minden</a> en provenance de celui de Senne (source : fichier cicr-01).</p>

<p>Après la guerre, il épouse à Saint-Servan, en Ille-et-Vilaine, Angèle Marie Louise AUVRAY. Il est décédé à Villeneuve-Saint-Georges, dans le Val-de-Marne, le 10 juin 1981 à l’âge de 89 ans.</p>

