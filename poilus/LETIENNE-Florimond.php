<h1>LETIENNE Florimond</h1>

<p><strong>Florimond LETIENNE</strong>, le beau frère d'<a href="FIEVET-Auguste-Victor.php" title="lien vers la page consacrée à Auguste FIEVET">Auguste</a> et <a href="FIEVET-Charles-Augustin.php" title="lien vers la page consacrée à Charles FIEVET">Charles FIEVET</a>, est né le 19 octobre 1873 à Roubaix (Nord), fils légitime de Henri Joseph LETIENNE et de Adéle Fileline DEGOBERT. Le 12 novembre 1898 à Roubaix, Florimond épouse Marie DELAUX, fille légitime d&#39;Adolphe (1856-1906) et Joséphine RICHOMME (1857 - ? ), et soeur de Nathalie DELAUX (1885 - 1952), mon arrière-arrière-grand-mère.</p>

<figure>
    <a href="images/letienne-florimond-1873.jpg" id="single_image" title="Acte de naissance de Florimond LETIENNE">
    <img src="miniatures/letienne-florimond-1873.jpg" alt="Acte de naissance de Florimond LETIENNE">
    </a>
    <figcaption>Acte de naissance de Florimond<br /> (source : Archives Départementales du Nord)</figcaption>
</figure> 

    <p>
        En 1906, Florimond est rattacheur. Il habite Cour Mulliez (Rue des Longues-Haies) à Roubaix, en compagnie de son épouse Marie Lucie et de leurs enfants : Léonie, Georges et Charles.</p>

<br/><h4>Florimond et la Grande Guerre</h4>
    
<p>Suite à la mobilisation, il arrive au corps le 2 août 1914 (peut-être le <strong>27e Régiment d'Artillerie</strong>). Ensuite, il passera successivement par le <strong>6e Régiment d'Artillerie à Pied</strong> (le 21 septembre 1915), le <strong>7e Régiment d'Artillerie</strong> à Pied (le 1 mars 1916), le <strong>11e Régiment d'Artillerie à Pied</strong> (le 25 décembre 1917) et enfin le <strong>13e Régiment d'Artillerie de Campagne</strong> (le 10 mars 1918).</p>

