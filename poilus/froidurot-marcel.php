    <h1 class="my-4">Marcel Auguste FROIDUROT</h1>
        
        <p class="text-justify">L'histoire de Marcel Auguste FROIDUROT commence le 12 mai 1891 à Semur-en-Auxois, une ville de Côte-d'Or où il voit le jour. Il est le fils légitime d'<a href="froidurot-alphonse.php" title="Biographie du poilu Alphonse FROIDUROT">Alphonse FROIDUROT</a> (plâtrier) et de Marie Blanche GIRARDOT, et le frère d'Alfred. Il est peintre en décor.</p>

        <figure class="text-center">
            <a href="sources/marcel-froidurot-naissance.jpg" data-fancybox="gallery" data-caption="Acte de naissance de Marcel">
            <img src="miniatures/marcel-froidurot-naissance.jpg" class="figure-img img-fluid img-thumbnail" alt="Acte de naissance de Marcel Auguste FROIDUROT"/></a>
            <figcaption class="figure-caption">Acte de naissance de Marcel FROIDUROT<br />(source: Archives départementales de Côte-d'Or)</figcaption>
        </figure>
        
        <p class="text-justify">Inscrit sous le n°36 de la liste du canton de Semur, il est incorporé à compté du 1er octobre 1912 et arrive au corps 10 jours plus tard. Il est blessé le 8 novembre 1913 en faisant son lit "par suite d'une chute sur le sol cimenté de la chambre" et "est atteint de contusion au genou droit avec épanchement musculaire".</p>

        <p class="text-justify">Une décision, prise à Besançon le 10 février 1914, le classe dans le service auxiliaire pour des raisons de santé, il est atteint d'hydrarthrose chronique du genoux droit. Le service auxilaire était destiné aux hommes qui étaient en moins bonne santé et ne pouvaient pas faire un réel service actif dans l'armée. Ils étaient alors cantonnés à des rôles "un peu plus legers" comme l'entretien des voies ferrées, les télégraphes ou encore les hopitaux. Ce diagnostic est confirmé par la commission médicale de Besançon le 7 décembre 1914, qui finira par le déclarer inapte à faire campagne le 24 août 1918 pour dyspepsie chronique.</p>

        <p class="text-justify">Durant la Grande Guerre, il fait deux séjours dans des hôpitaux pour cause de maladie. D'abord à Charmont du 4 au 6 mai 1914 puis à l'hôpitax de la Butte de Besançon du 9 juin 1918 au 20 juillet 1918. Selon le site <a href="https://aetdebesancon.home.blog/" title="Histoire et historiettes à partir de la presse de Besançon">Aetdebesancon</a>, il s'agit d'un <a href="../hopitaux/hopitaux.php" title="Les hôpitaux et le service de santé pendant la Première Guerre Mondiale">hôpital temporaire</a> installé dans la caserne Brun. Trés actif dès le début de la guerre, il est rattaché à l'Hôpital Saint Jacques, située dans le centre ville. Il rejoint le dépot le 11 octobre 1918 après une convalescence.</p>

        <p class="text-justify">Il est envoyé en congés illimités de démobilisation le 24 août 1919 et se retire à Besançon, au 4 bis de la rue Charles Mortier.</p>

        <figure class="text-center">
            <a href="../hopitaux/images/caserne-brun.jpg" data-fancybox="gallery" data-caption="La caserne brun, dite La Butte, à Besançon">
            <img src="../hopitaux/miniatures/caserne-brun.jpg" class="figure-img img-fluid img-thumbnail" alt="La caserne Brun à Besançon"/></a>
            <figcaption class="figure-caption">La caserne brun (La Butte) à Besançon<br />(source: Delcampe)</figcaption>
        </figure>

        <p class="text-justify">Au niveau de sa vie privée, je n'ai pas grand chose à vous raconter, mais après son retour à la vie civile, il vit quelques années rue du Prévost à Beauvais (Oise). Le 21 octobre 1915, il avait épousé, à la mairie de Besançon, Adélaïde DUPAS. Il est décédé à Semur le 4 décembre 1965.</p>

