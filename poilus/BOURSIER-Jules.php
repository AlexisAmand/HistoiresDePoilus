<h1>BOURSIER Jules Henri</h1>

<p><strong>Jules Henri BOURSIER</strong> est né à Onnaing (Nord) le 7 décembre 1879, fils légitime de Edmond François BOURSIER, cultivateur, et de Julia JUDES. En 1906, il est chaudronnier et vit chez ses parents à Onnaing (Rue De La Chevauchoire), avec son frère Edmond. Le 19 novembre de la même année, il épouse Suzanne Hyacinthe GODIN (1879-1939). </p> 
      
    <figure>
        <a href="images/BOURSIER-Jules-Henri-naissance-1879.jpg" id="single_image" title="acte de naissance de Jules Henri BOURSIER">
        <img src="miniatures/BOURSIER-Jules-Henri-naissance-1879.jpg" alt="acte de naissance de Jules Henri BOURSIER"/>
        </a>
        <figcaption>Acte de naissance de Jules Henri BOURSIER<br/>(cliquez pour agrandir - source: Archives Départementales du Nord)</figcaption>
    </figure>
    
<p><strong>Jules Henri BOURSIER</strong> est le poilu avec lequel ma parenté est la plus éloignée car il était marié avec Suzanne GODIN, descendante de quatre de mes ancêtres ayant vécu à la fin du XVIIe et au début du XVIIIe siècle. J'ai trouvé la photo ci-dessous grâce à un membre de Geneanet. Il me reste à comprendre pourquoi il y a 16 sur son col, alors qu'il faisait parti du <strong>1er Régiment d'Artillerie à Pied</strong> pendant la Première Guerre Mondiale. Peut-être que la photo n'est pas contemporaine au conflit ?</p>

    <figure>
      <a href="portraits/BOURSIER-Jules-Henri.jpg" id="single_image" title="portrait de Jules Henri BOURSIER">
      <img src="miniatures/BOURSIER-Jules-Henri.jpg" height="300" alt="portrait de Jules Henri BOURSIER"/>
      </a>
      <figcaption>Jules Henri BOURSIER<br/>(cliquez pour agrandir - source: Kamalaa/Geneanet)</figcaption>
    </figure>

<br/><h4> La mobilisation d'août 1914</h4>

<p><strong>Jules</strong> est âgé de 35 ans lors de mobilisation. Il est rappelé à l'activité et arrive au <strong>1er Régiment d'Artillerie à Pied</strong> le 2 aout 1914. La portion centrale de ce régiment était à Dunkerque avec les 4e, 6e et 7e batteries, la 3e était à Boulogne, la 5e à Calais et enfin les 1ere et 2e étaient à Maubeuge. </p>
    <p>Pas moins de 3 780 000 hommes sont mobilisés en août 1914. Au total, durant toute la guerre, environ 8 410 000 soldats et marins français seront mobilisés, dont 7 % de soldats indigènes venant des colonies de l'Empire. Parmi tous ces soldats, 1 397 800 sont <a href="../releves.php">morts pour la France</a> et 4 266 000 ont été bléssés au combat.</p>

