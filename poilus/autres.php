<h1>A venir...</h1>

<p>Cette rubrique est destinée à accueillir toutes les personnes que je qualifie de &quot;mobilisables&quot;, en clair il s'agit des hommes qui étaient en âge de participer à la Première Guerre Mondiale pour lesquelles je n'ai pas encore (ou alors très peu) d'informations: Soit elles ne sont pas disponibles, soit je n'ai pas encore eu le temps de chercher.</p>
<p>N'hésitez pas à me laisser un message si vous avez des infos :-)</p>
    <p></p>

    <figure>
    <a href="images/jumelles-poilus-1914.jpg" id="single_image" title=" ">
