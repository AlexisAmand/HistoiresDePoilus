<h1>Le camp d'internement d'Amersfoort</h1>

<p>Ce camp d'internement était situé à Amersfoort, une ville de la province d'Utrecht, aux Pays-Bas.<sup style="font-size:8px;">1</sup></p>

<figure>  
    <a href="images/Amersfoort/amersfoort-01.jpg" id="single_image" title="Le camp de Amersfoort">
    <img src="miniatures/Amersfoort/amersfoort-01.jpg" alt="Le camp d'internement d'Amersfoort pendant la Première Guerre Mondiale"></a>
    <figcaption>Le camp de Amersfoort<br />(Carte postale)</figcaption>
</figure>

<p>Le journal Le Telegraf du 10 juillet 1918 rapporte que "d<em>es milliers d'internés occupent encore à Amersfoort les baraquements dans lesquels ils ont été enfermés en 1914. La situation hygiénique de ces baraques et déplorable. Les paillasses sont sales et à moitié pourries; il n'y a pas de ventilation la nuit et la lumière n'entre que par de toutes petites fenêtres. La plupart des internés sont inoccupés et passent la plus grande partie de la journée couchés sur leurs matelas. Les vêtements manquent, surtout le linge. comme les hommes n'ont qu'un pantalon et une seule veste avec lesquels ils font toutes les corvées, ils ne sont jamais propres, même pour sortir. Les internés belges ne savent que faire de leur temps. Ils ont bien un petit théâtre, une cantine et un cinéma, il y a aussi des écoles, mais le moral est si bas qu'ils préfèrent se laisser aller à l'abandon, à l'oisiveté. Ce que les internés demandent, c'est du travail. (...) C'est ainsi que les boulangers et d'autres gens de métier ont obtenu de l'ouvrage dans les mines, alors que des mineurs doivent rester inactifs au
    </em><a href="camp-zeist.php"><em>camp de Zeist</em></a><em> à cause de leurs opinions politiques ou de leurs convictions religieuses.<sup style="font-size:8px;">2</sup>&quot;</em></p>

<p style="font-size:12px;">
<sup>1</sup>
Durant la Seconde Guerre Mondiale, un camp de concentration sera installé le long de l'autoroute Utrecht-Amersfoort. Des milliers de civils belges et hollandais y seront torturés et executés. Ce camp sera appelé "Polizeiliches Durchgangslager Amersfoort" et sera dirigé par Joseph Kotälla condamné à mort à la fin de la guerre.</p>

<p style="font-size:12px;">
<sup>2</sup>
La traduction de l'article du Telegraph est parue dans "Le Droit des peuples : organe d'action socialiste nationale et internationale et de défense des réfugiés alsaciens, lorrains, belges, français" du 2 aout 1918. 
</p>

