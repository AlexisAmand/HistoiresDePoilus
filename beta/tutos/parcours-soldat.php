            <h1>Comment retrouver le parcours d’un soldat ?</h1>

            <p>Ce petit guide a pour but de vous aider à trouver des informations sur un individu qui a participé à la Première Guerre Mondiale. Il ne s’agit d’une méthode, mais plutôt de diverses pistes que vous pouvez tester. Je ne pourrais pas être exhaustif car chaque cas est différent en fonction, par exemple, du corps d’armée ou de la classe de mobilisation. Pour ce tutoriel, je vais mettre de côté le cas où vous avez déjà des informations, si vous en avez, on va dire que ça vous fera un petit plus.</p>

            <h2>1) Les fiches matricules</h2>

            <p>La fiche matricule d’un individu est un résumé de son parcours militaire. La plupart du temps, elle est établie pendant la vingtième année d’un homme, on parle alors de <strong>classe</strong>, en fonction de son domicile, on parle de <strong>bureau de recrutement</strong>. Par exemple, mes ancêtres qui ont vécu dans le village de Sebourg (Nord) dépendent du bureau de recrutement de Valenciennes, alors que ceux qui ont vécu dans le petit village de Bétheniville (Marne) dépendent de celui de Reims. Les individus d’une même classe et d’un même bureau sont ensuite regroupés au sein de listes alphabétiques appelées, par exemple, tables alphabétiques sur le site des <a href="#">Archives Départementales du Nord</a>. Pour le Bureau de Dunkerque (Nord), la table alphabétique de 1884 ressemble à ça :</p>

            <figure>
                <img src="images/dunkerque-1884.jpg" style="height:60%;"> 
                <figcaption>Extrait...</figcaption>
            </figure>

            <p>Chaque jeune homme se voit attribuer un nombre, que l’on appelle « matricule », c’est celui qui va vous aider à trouver sa fiche.  Si vous n’avez pas trouvé votre ancêtre dans la classe qui correspond à l’année de ses 20 ans, jetez un coup d’œil à l’année de ses 19 ou 21 ans, on ne sait jamais. Et surtout, j’insiste bien sur le fait qu’il ne faut pas  chercher en fonction du lieu de naissance, mais bien sur le domicile du conscrit. Tout va bien jusque là ? Super ! On continue.</p>

            <p>Si on résume, dans l’état actuel de vos recherches, vous avez les éléments suivants :</p>

            <ul>
                <li>Un bureau, qui dépend du lieu où vivait le jeune homme</li>
                <li>Une classe, la plupart du temps elle correspond à l’année de ses 20 ans</li>
                <li>un numéro de matricule que vous avez trouvé sur les « Tables alphabétiques »</li>
            </ul>

            <h2>2) le cas des prisonniers de guerre</h2>

            <p>Si, en parcourant la fiche matricule que vous venez de trouver, vous avez découvert que votre aïeul a été prisonnier en Allemagne (ou ailleurs…) pendant la Grande Guerre, vous avez une chance de trouver des informations complémentaires sur le site de la Croix Rouge :</p>

            <a href="http://grandeguerre.icrc.org/fr">http://grandeguerre.icrc.org/fr</a>

            <figure>
                <img src="images/grandeguerre-01.jpg" style="width:60%;"> 
                <figcaption>Extrait...</figcaption>
            </figure>

            <p>Je vous laisse faire une recherche via le formulaire proposé sur la page d’accueil du site. Si le conscrit pour lequel vous faites des recherches est présent dans la base de données du CICR, les résultats vont apparaître sous la forme de petites fiches classées par ordre alphabétique. N’hésitez pas à faire des essais avec des variantes du patronyme.</p>

            <figure>
                <img src="images/grandeguerre-01.jpg" style="width:60%;"> 
