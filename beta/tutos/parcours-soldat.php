            <p>Chaque fiche contient un certain nombre de références (sur l’exemple ci-dessus R100124) qui renvoient à leur tour vers d’autre document que je vous laisse découvrir.</p>

            <h2>3) le cas des morts pour la France</h2>

            <h3>3.1) Mémoire des Hommes</h3>

            <p>Mémoire des Hommes est un site qui dépend du Service Historique de la Défense, via le Ministère des Armées. Il s’agit d’une base de données qui regroupe une grande quantité de données sur les personnes qui ont servi au cours des différents conflits qui ont jalonné notre histoire. La base la plus utilisée par les généalogistes est sans aucune doute la base des « Morts pour la France de la Première Guerre mondiale » :</p>

            https://www.memoiredeshommes.sga.defense.gouv.fr/fr/article.php?larub=24&titre=morts-pour-la-france-de-la-premiere-guerre-mondiale 

            <figure>
