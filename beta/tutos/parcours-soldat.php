            <p>Un clic sur le gros bouton bleu nommé « faire une recherche » vous donne accés un petit formulaire de recherche, pour ce petit tutoriel, j’ai déjà rempli le champ  « nom » avec le patronyme de l’un de mes arrière-arrière-grand-pères. A noter au passage que presque 1,5 millions de  soldats morts pour la France ont été indexés par des bénévoles via un mouvement initié par le site http://www.1jour1poilu.com</p> 

