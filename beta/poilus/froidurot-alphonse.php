        <h1>Alphonse FROIDUROT</h1>
        
        <p>Alphonse FROIDUROT voit le jour le 19 juillet 1869 dans la maison de ses parents située Rue de Montmuzard à Dijon (Côte-d'Or), fils légitime de Claude FROIDUROT et Alix MAURICE. Le 6 décmebre 1888, il épouse Marie Barbe GIRARDOT à Semur-en-Auxois (Côte-d'Or) où le couple par s'installer et où naît le petit <a href="FROIDUROT-Alfred.php" title="biographie du resistant Alfred Froidurot">Alfred</a>, qui sera un des nombreux héros anonymes de la Seconde Guerre Mondiale, le 29 janvier 1898.</p>
        
        <figure>
            <a href="sources/froidurot-alphonse.jpg" data-fancybox="gallery" data-caption="Acte de naissance d'Alphonse FROIDUROT">
            <img src="miniatures/froidurot-alphonse.jpg" alt="Extrait de la fiche matricule d'Alphonse FROIDUROT"/></a>
            <figcaption>Acte de naissance d'Alphonse FROIDUROT<br />(Source: Archives Départementales de la Côte-d'Or)</figcaption>
        </figure>
     
        <p>Matricule n°327 au bureau Dijon, il est rappelé à l'activité par la mobilisation générale du 2 août 1914, rejoint comme la plupart des hommes de sa génération, la "Territoriale" et est affecté dès le début du mois d'août à la garde des voies des communications. Il est renvoyé dans ses foyers le 28 janvier 1915, sans doute à cause de son âge, mais rien n'est sûr dans l'état actuel de mes recherches. Quelques mois plus tard, le 2 juillet 1915 à Dijon, son fils Alfred a tout juste 17 ans quand il s'engage comme volontaire pour la durée de la guerre au 17e Régiment de Dragons.</p>

