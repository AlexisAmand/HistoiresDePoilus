        <p class="text-justify">Je vous laisse faire une recherche via le formulaire proposé sur la page d’accueil du site. Si le conscrit pour lequel vous faites des recherches est présent dans la base de données du CICR, les résultats vont apparaître sous la forme de petites fiches classées par ordre alphabétique. N’hésitez pas à faire des essais avec des variantes du patronyme.</p>

