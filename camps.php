<h1>Les camps de prisonniers en Allemagne</h1>

<p>Quand la guerre éclate en aout 1914, L'Empire Allemand est certain que sa victoire sera aussi rapide que celle de 1870. Seulement très vite, il doit faire face à un afflux de prisonniers qu&#39;il n&#39;avait pas prévu. Cette situation l&#39;oblige à ouvrir des camps un peu partout sur son territoire. En cette fin d&#39;année 1914, les soldats prisonniers dorment dans la paille, dans des huttes de fortune ou dans des trous cressés à même le sol pour&nbsp; s&#39;abriter du froid et de la pluie. L&#39;hygiène est déplorable, et très vite, des <strong>épidémies de Typhus</strong> apparaissent. Faute d'être prêt à nourrir et à loger un si grand nombre de prisonniers, l'hiver 1914 sera mortel pour des centaines d'hommes. Il faudra attendre l&#39;année 1915 pour que la situation s&#39;améliore un peu.</p>

<figure>
    <a href="camps/images/friedrichsfeld-prisonniers-français.jpg" id="single_image" title="Des prisonniers français au camp de Friedrichsfeld">
    <img src="camps/miniatures/friedrichsfeld-prisonniers-français.jpg"  alt="Des prisonniers français au camp de Friedrichsfeld"/>
    </a>
    <figcaption>Des prisonniers au camp de Friedrichsfeld (Allemagne)<br/>(cliquez pour agrandir)</figcaption>
</figure>

<p>La <a href="pdf/convention-1907.pdf">Convention de la Haye</a> (1899 et 1907) autorisait le travail des prisonniers uniquement s&#39;il s&#39;agissait d&#39;une activité &quot;non excesive&quot; et bien sûr dans lien direct avec le conflit pour éviter que les prisonniers soient les victimes involontaires d&#39;un tir fratricide. Toutefois, dès 1916, cet article de la Convention est violé par la plupart des belligérants. C&#39;est ainsi qu&#39;en 1917, l&#39;Allemagne met en place <strong>des dizaines de milliers de &quot;kommandos&quot;</strong> qui obligent les prisonniers à travailler à la campagne ou dans les industries allemandes. On estime alors que le nombre de prisonniers alliés qui étaient réellement détenu dans des camps à un peu moins de 15%.</p>

<figure>
    <img src="images/la-haye-01.jpg" alt="La Première Conférence de La Haye en 1899"/>
    <figcaption>La Première Conférence de La Haye en 1899</figcaption>
</figure>

    <p>Les prisonniers étaient gérés comme véritable armée: Ils étaient (la plupart du temps) logés dans des casernes, vivaient sous les ordres de leurs gardiens et étaient soumis au même réglement et à la même discipline que ceux qui étaient en vigueurs dans l&#39;armée de leurs géoliers. On estime <strong>le nombre de soldats prisonniers en 1918 à 6 637 000</strong> parmi lesquels 2 500 000 sont retenus par l'Allemagne, 2 250 000 par la Russie, 916 000 par l'Autriche, 350 000 par la France, 328 000 par le Royaume-Uni, et 43 000 par les Etats-Unis.</p>
    
