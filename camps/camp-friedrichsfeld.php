    <h1 class="my-4">Le camp de prisonniers de Friedrichsfeld</h1>

        <p class="text-justify"><em><strong>Mise en garde:</strong> Certaines photographies portent la mention "source allemande", cela signifie qu'elles sont issues d'ouvrages ou d'archives allemandes et qu'elles ne sont donc pas très objectives.</em></p><p class="text-justify"></p><p class="text-justify">Le <strong>camp de Friedrichsfeld</strong> était  en Rhénanie du Nord, à proximité des villes de Duisbourg et Cologne ainsi qu'à proximité de la frontière entre l'Allemagne et les Pays-Bas. Il disposait d'un Lazaret (hôpital militaire) et d'une chapelle. Dans <cite><a href="../webographie.php" title="selection de livres et de sites pour les recherches sur la Première Guerre Mondiale">Westfalen in Ersten Weltkrieg</a></cite>, l'historien allemand Rainer PÖPPINGHEGE évalue à 79161 le nombre de soldats prisonniers dans le camp de Friedrichsfeld à la fin de la Première Guerre Mondiale.</p>

        <figure class="text-center">
            <img src="miniatures/Friedrichsfeld/carte-camp-friedrichsfeld.jpg" class="figure-img img-fluid img-thumbnail" data-fancybox="gallery" alt="carte de l'Allemagne pour localiser le camp de prisonnier de Friedrichsfeld">
            <figcaption class="figure-caption">Localisation du camp de Friedrichsfeld<br />(source pour le fond de carte: BNF - Gallica)</figcaption>
        </figure>

        <div id="Major_Bach"></div>

        <p class="text-justify">Dans un rapport adressé au Ministère de la Guerre, le médecin major de 1e classe CHON, du 14e Régiment de Hussards, fait prisonnier à Ethe (Belgique) le 22 août 1914, emprisonné aux camps de Friedrichsfeld et Senne, puis ramené en Suisse pour raison de santé, rapporte que <em>"le sous-chef du camp [de Friedrichsfeld], le Major BACH, avait donné l'ordre de fusiller les fuyard et promettait une indemnité de 70 marks à tout homme qui rapporterait le cadavre d'un français. Le fait nous a été rapporté par un ordonnance allemande qui se ventait d'avoir abattu un prisonnier français dans ces conditions".</em></p>

        <figure class="text-center">
            <a href="images/friedrichsfeld-patates.jpg" data-fancybox="gallery" data-caption="Camp de Friedrichsfeld: Epluchage de pommes de terre dans la cour de l'hôpital">
            <img src="miniatures/friedrichsfeld-patates.jpg" class="figure-img img-fluid img-thumbnail" alt="épluchage de pommes de terre dans la cour de l'hôpital du camp de Friedrichsfeld"></a>
            <figcaption class="figure-caption">Epluchages de pommes de terre dans la cour de l'hôpital<br />(source allemande)</figcaption>
        </figure>

        <figure class="text-center">
            <a href="images/friedrichsfeld-lazaret.jpg" data-fancybox="gallery" data-caption="Camp de Friedrichsfeld: Intérieur d'une baraque servant de Lazaret">
            <img src="miniatures/friedrichsfeld-lazaret.jpg" class="figure-img img-fluid img-thumbnail" alt="intérieur d'une baraque servant de lazaret au camp de Friedrichsfeld"></a>
            <figcaption class="figure-caption">Intérieur d'une grande baraque servant de lazaret<br />(source allemande)</figcaption>
        </figure>

        <p class="text-justify">L'espace occupé par les prisonniers comprend une superficie de 25 hectares entourée d'une triple rangée de fils de fer barbelés. Des baraquements en planches, construits par les prisonniers eux-mêmes, comptent en mai 1915 pas moins de 20 000 hommes dont 16000 français, 3000 russes, 500 belges, 300 anglais.</p>

        <figure class="text-center">
            <a href="images/friedrichsfeld-prisonniers-français.jpg" data-fancybox="gallery" data-caption="Des prisonniers français au camp de Friedrichsfeld">
            <img src="miniatures/friedrichsfeld-prisonniers-français.jpg" class="figure-img img-fluid img-thumbnail"  alt="Des prisonniers français au camp de Friedrichsfeld"/></a>
            <figcaption class="figure-caption">Des prisonniers au camp de Friedrichsfeld<br />(cliquez pour agrandir)</figcaption>
        </figure>

        <p class="text-justify">Quand <a href="../poilus.php" title="Les biographies de soldat de la Première Guerre Mondiale">Jules Henri BOURSIER</a>, <a href="../poilus/CARLIER-Druon-Joseph.php" title="biographie du soldat Druon Joseph CARLIER">Druon Joseph CARLIER</a>, <a href="../poilus/JOLY-Noel-Jules.php" title="Biographie du soldat Noël JOLY">Noël Jules JOLY</a> et leurs compagnons français arrivent dans le camp de Friedrichsfeld en cette fin d'année 1914, il est juste indiqué par une clôture de fils de fer, mais rien n'est construit; pendant de longues semaines, les hommes doivent coucher en plein air, sur la paille, avec une seule couverture. Sous la direction d'ouvriers civils, les prisonniers sont employés à la construction de baraquements qui sont habités dès le mois de décembre 1914. A cette époque, ils reçoivent une enveloppe de paillasse contenant de la fibre de bois et de la paille (qui ne sera jamais renouvelée) et une seconde couverture. Après son achèvement, le camp est une véritable petite ville, constituée par des baraques de planches, dont chacune est divisée en 2 chambrées par une cloison.</p><p class="text-justify">Dans l'exemplaire n°202 de l'ouvrage intitulé <cite><a href="http://gallicalabs.bnf.fr/ark:/12148/bpt6k65295400" title="Le sort des prisonniers français en allemagne pendant la Grande Guerre">Le sort des prisonniers français en Allemagne</a></cite>, l'auteur explique qu'à Friedrichsfeld, il y avait une baraque spéciale, close de treillages, où restaient enfermés nuit et jour les prisonniers qui se refusent au travail; au mois de décembre 1915, 25 réfractaires avaient été laissés toute une nuit, en tenue de travail, sur le bord du Rhin, au garde-à-vous, les pieds posés sur des cailloux. Quelques pages plus loin, l'auteur nous décrit l'impuissance des prisonniers français contre les épidémies, notamment dans les camps de <a href="camp-darmstadt.php" title="le camp de prisonniers de Darmstadt">Darmstadt</a>, <a href="camp-friedrichsfeld.php" title="le camp de prisonniers de Friedrichsfeld">Friedrichsfeld</a>, Koenigsbrück, Merseburg et Zoosen où la faim non apaisée, l'inanition et la misère psychologique qui en a résulté ont provoqué un développement inouï de la tuberculose et une mortalité effrayante.</p>

        <figure class="text-center">
            <a href="images/friedrichsfeld01.jpg" data-fancybox="gallery" data-caption="Des prisonniers dans le camp de Friedrichsfeld">
            <img src="miniatures/friedrichsfeld01.jpg" class="figure-img img-fluid img-thumbnail" alt="Des prisonniers dans le camp de Friedrichsfeld"></a>
        </figure>

        <p class="text-justify">Dans le "Bulletin religieux de l'Archidiocèse de Rouen", L. J. raconte qu' <em>"à Friedrichsfeld (...) les prisonniers sont l'objet de la curiosité indiscrète des indigènes qui viennent les voir travailler et manger, comme les enfants de chez nous vont au Jardin des Plantes assister au repas des fauves. Et cela, avec l'isolement, l'ignorance où ils sont des leurs, est ce dont souffrent le plus Belges et Français."</em>. Le n°23 de "Nouvelles de France" parut le 8 juin 1916, complète avec des informations sur l'alimentation des prisonniers guerre qui étaient internés au <strong>camp de Friedrichsfeld</strong>.</p>

