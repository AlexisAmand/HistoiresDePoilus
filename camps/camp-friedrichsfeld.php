<h1>Le camp de prisonniers de Friedrichsfeld</h1>

<p><em><strong>Mise en garde:</strong> Certaines photographies portent la mention "source allemande", cela signifie qu&#39;elles sont issues d&#39;ouvrages ou d&#39;archives allemandes et qu&#39;elles ne sont donc pas très objectives.</em></p><p></p><p>Le <strong>camp de Friedrichsfeld</strong> était  en Rhénanie du Nord, à proximité des villes de Duisbourg et Cologne ainsi qu'à proximité de la frontière entre l'Allemagne et les Pays-Bas. Il disposait d'un Lazarett (hôpital militaire) et d'une chapelle. Dans <cite><a href="../webographie.php">Westfalen in Ersten Weltkrieg</a></cite>, l&#39;historien allemand Rainer PÖPPINGHEGE évalue à 79161 le nombre de soldats prisonniers dans le camp de Friedrichsfeld à la fin de la Première Guerre Mondiale.</p>
    
<figure>
    <img src="miniatures/Friedrichsfeld/carte-camp-friedrichsfeld.jpg" id="single_image" alt="carte de l'Allemagne pour localiser le camp de prisonnier de Friedrichsfeld">
    <figcaption>Localisation du camp de Friedrichsfeld<br/>(source: BNF - Gallica)</figcaption>
</figure>      
    
<p>Dans un rapport adressé au Ministère de la Guerre le 20 août 1914, le médecin major de 1e classe CHON, du 14e Régiment de Hussard, fait prisonnier à Ethe (Belgique) le 22 août 1914, emprisonné aux camps de Friedrichsfeld et Senne, puis ramené en Suisse pour raison de santé, rapporte que <em>&quot;le sous-chef du camp [de Friedrichsfeld], le Major BACH, avait donné l&#39;ordre de fusiller les fuyard et promettait une indemnité de 70 marks à tout homme qui rapporterait le cadavre d&#39;un français. Le fait nous a été rapporté par un ordonnance allemande qui se ventait d&#39;avoir abattu un prisonnier français dans ces conditions&quot;.</em></p>

<figure>
    <a href="images/friedrichsfeld-patates.jpg" id="single_image" title="Camp de Friedrichsfeld: Epluchage de pommes de terre dans la cour de l'hôpital">
    <img src="miniatures/friedrichsfeld-patates.jpg" alt="épluchage de pommes de terre dans la cour de l'hôpital du camp de Friedrichsfeld">
    </a>
    <figcaption>Epluchages de pommes de terre dans la cour de l'hôpital<br />(source allemande)</figcaption>   
</figure>

<figure>
    <a href="images/friedrichsfeld-lazaret.jpg" id="single_image" title="Camp de Friedrichsfeld: Intérieur d'une baraque servant de Lazaret">
    <img src="miniatures/friedrichsfeld-lazaret.jpg" alt="intérieur d'une baraque servant de lazaret au camp de Friedrichsfeld">
    </a>
    <figcaption>Intérieur d'une grande baraque servant de lazaret<br />(source allemande)</figcaption>   
</figure>

<p>L'espace occupé par les prisonniers comprend une superficie de 25 hectares entourée d'une triple rangée de fils de fer barbelés. Des baraquements en planches, construits par les prisonniers eux-mêmes, comptent en mai 1915 pas moins de 20 000 hommes dont 16000 français, 3000 russes, 500 belges, 300 anglais.</p>

<figure>
    <a href="images/friedrichsfeld-prisonniers-français.jpg" id="single_image" title="Des prisonniers français au camp de Friedrichsfeld">
    <img src="miniatures/friedrichsfeld-prisonniers-français.jpg"  alt="Des prisonniers français au camp de Friedrichsfeld"/>
    </a>
    <figcaption>Des prisonniers au camp de Friedrichsfeld<br/>(cliquez pour agrandir)</figcaption>
</figure>

<p>Quand <a href="../poilus.php">Jules Henri BOURSIER</a>, <a href="../poilus/CARLIER-Druon-Joseph.php">Druon Joseph CARLIER</a>, <a href="../poilus/JOLY-Noel-Jules.php">Noël Jules JOLY</a> et leurs compagnons français arrivent dans le camps de Friedrichsfeld en cette fin d&#39;année 1914, il est juste indiqué par une clôture de fils de fer, mais rien n&#39;est construit; pendant de longues semaines, les hommes doivent coucher en plein air, sur la paille, avec une seule couverture. Sous la direction d&#39;ouvriers civils, les prisonniers sont employés à la construction de baraquements qui sont habités dès le mois de décembre 1914. A cette époque, ils reçoivent une enveloppe de paillasse contenant de la fibre de bois et de la paille (qui ne sera jamais renouvelée) et une seconde couverture. Après son achèvement, le camp est une véritable petite ville, constituée par des baraques de planches, dont chacune est divisée en 2 chambrées par une cloison.</p><p>Dans l&#39;exemplaire n°202 de l&#39;ouvrage intitulé <cite><a href="http://gallicalabs.bnf.fr/ark:/12148/bpt6k65295400">Le sort des prisonniers français en Allemagne</a></cite>, l&#39;auteur explique qu&#39;à Friedrichsfeld, il y avait une baraque spéciale, close de treillages, où restaient enfermés nuit et jour les prisonniers qui se refusent au travail; au mois de décembre 1915, 25 réfractaires avaient été laissés toute une nuit, en tenue de travail, sur le bord du Rhin, au garde-à-vous, les pieds posés sur des cailloux. Quelques pages plus loin, l'auteur nous décrit l'impuissance des prisonniers français contre les épidémies, notamment dans les camps de <a href="camp-darmstadt.php">Darmstadt</a>, <a href="camp-friedrichsfeld.php">Friedrichsfeld</a>, Koenigsbrück, Merseburg et Zoosen où la faim non apaisée, l'inanition et la misère psychologique qui en a résulté ont provoqué un développement inouï de la tuberculose et une mortalité effrayante.</p>

<figure>
    <a href="images/friedrichsfeld01.jpg" id="single_image" title="Des prisonniers dans le camp de Friedrichsfeld">
    <img src="miniatures/friedrichsfeld01.jpg" alt="Des prisonniers dans le camp de Friedrichsfeld">
    </a>
</figure>

<p>Dans le &quot;Bulletin religieux de l'Archidiocèse de Rouen&quot;, L. J. raconte qu' <em>&quot;à Friedrichsfeld (...) les prisonniers sont l'objet de la curiosité indiscrète des indigènes qui viennent les voir travailler et manger, comme les enfants de chez nous vont au Jardin des Plantes assister au repas des fauves. Et cela, avec l'isolement, l'ignorance où ils sont des leurs, est ce dont souffrent le plus Belges et Français.&quot;</em>. Le n°23 de &quot;Nouvelles de France&quot; parut le 8 juin 1916, complète avec des informations sur l'alimentation des prisonniers guerre qui étaient internés au <strong>camp de Friedrichsfeld</strong>.</p>

