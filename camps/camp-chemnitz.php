    <h1 class="my-4">Le camp de prisonniers de Chemnitz</h1>

        <p class="text-justify"><em><strong>Mise en garde:</strong> Certaines photographies portent la mention "source allemande", cela signifie qu'elles sont issues d'ouvrages ou d'archives allemandes et qu'elles ne sont donc pas très objectives.</em></p>

        <p class="text-justify">Ce camp est situé à Chemnitz, dans le Vogtland, une région située à l'est de l'Allemagne, à la limite de la Saxe et de la Bavière.</p>

        <figure class="text-center">
            <a href="images/Chemnitz/carte-camp-chemnitz.jpg" data-fancybox="gallery" data-caption="Localisation du camp de Chemnitz"> 
            <img src="miniatures/Chemnitz/carte-camp-chemnitz.jpg" class="figure-img img-fluid img-thumbnail" alt="carte de l'Allemagne pour localiser le camp de prisonnier de Chemnitz"></a>
            <figcaption class="figure-caption">Localisation du camp de Chemnitz<br />(source pour le fond de carte: BNF - Gallica)</figcaption>
        </figure>

        <p class="text-justify">En novembre 1915, l'Office d'information des oeuvres de secours aux prisonniers de guerre est informé que tous les prisonniers civils appartenant au camp de Chemnitz viennent d'être transférés au camp de Grofsporitscg Bei Zittau (Saxe). Lors de la visite du camp le 12, le dépôt de prisonniers est installé à la caserne Friedrich August, elle-même située dans le quartier Ebensdorf.</p>

        <figure class="text-center">
            <img src="miniatures/Chemnitz/caserne-friedrich-august.jpg" class="figure-img img-fluid img-thumbnail" alt="La caserne Friedrich August à Chemnitz">
            <figcaption class="figure-caption">La Caserne Friedrich August<br />(source : Delcampe)</figcaption>
        </figure>

        <figure class="text-center">
            <a href="images/chemnitz-census-01.jpg" data-fancybox="gallery" data-caption="Répartition des prisonniers en 1915">
            <img src="miniatures/chemnitz-census-01.jpg" class="figure-img img-fluid img-thumbnail" alt="répartition des prisonniers en fonction des nationalités à l'intérieur du camp de Chemnitz"/></a>
            <figcaption class="figure-caption">Répartition des prisonniers en 1915<br />(Cliquez pour agrandir)</figcaption>
        </figure>

        <p class="text-justify">Des maladies comme le typhus ou le choléra font très vite leur apparition dans les camps de prisonniers comme celui de Chemnitz où survit désormais <a href="../poilus/GRAIN-Louis.php" title="Biographie du soldat Louis GRAIN">Louis GRAIN</a>. Le confinement des logements, le nombre de prisonniers par baraque qui est en moyenne de 250, expliquent en partie le phénomène car l'atmosphère viciée se renouvelle très peu. Dans sa thèse "Gefangen im Großen Krieg", Uta Hinz explique qu'en février 1915, le camp de Chemnitz a été mis sous quarantaine. Un des prisonniers avait alors écrit que les seules voitures qui s'approchent du camp étaient celles qui transportaient les cercueils. Au total, on estime à 44732 le nombre de prisonniers de guerre ayant été victimes du typhus.</p>

        <figure class="text-center">
            <a href="images/chemnitz-cholera.jpg" data-fancybox="gallery" data-caption="La vaccination contre le choléra">
            <img src="miniatures/chemnitz-cholera.jpg" class="figure-img img-fluid img-thumbnail" alt="la vaccination contre le choléra au camp de Chemnitz"></a>
            <figcaption class="figure-caption">La vaccination contre le choléra<br />(source allemande)</figcaption>
        </figure>

        <p class="text-justify">La même année, suite à une visite dans les camps de prisonniers français en Allemagne, les délégués du gouvernement espagnol décrivent les baraques du camp de Chemnitz comme <em>"de grandes salles ayant 15m de large sur 120m de long. Les lits (matelas de fibres de bois et 2 couvertures) sont placés perpendiculairement au sens de la longueur; au centre et sur toute la longueur du local sont installés des tables et des bancs. Il reste néanmoins un espace suffisant pour le passage".</em></p> 

        <figure class="text-center">
            <a href="images/Chemnitz/chemnitz-01.jpg" data-fancybox="gallery" data-caption="L'intérieur d'une baraque de Chemnitz">
            <img src="miniatures/Chemnitz/chemnitz-01.jpg" class="figure-img img-fluid img-thumbnail" alt="L'intérieur d'une baraque du camp de prisonniers de Chemnitz"></a>
            <figcaption class="figure-caption">L'intérieur d'une baraque<br />(source : Delcampe)</figcaption>
        </figure>

        <p class="text-justify">Ils précisent aussi que les prisonniers sont <em>"affectés à différents métiers comme dans les autres dépôts ; le salaire des ouvriers travaillant dans  les fabriques est de 1 mark par jour en dehors des frais de leur entretien"</em>.</p>

        <figure class="text-center">
            <a href="images/chemnitz-01.jpg" data-fancybox="gallery" data-caption="L'atelier de charronnerie">
            <img src="miniatures/chemnitz-01.jpg" class="figure-img img-fluid img-thumbnail" alt="L'atelier de charronnerie du camp de Chemnitz"></a>
            <figcaption class="figure-caption">L'atelier de charronnerie<br />(source : Delcampe)</figcaption>
        </figure>

        <figure class="text-center">
            <a href="images/chemnitz-concert.jpg" data-fancybox="gallery" data-caption="Concert de dimanche des prisonniers">
            <img src="miniatures/chemnitz-concert.jpg" class="figure-img img-fluid img-thumbnail" alt="Le concert du dimanche au camp de Chemnitz"></a>
            <figcaption class="figure-caption">Concert de dimanche des prisonniers<br />(source allemande)</figcaption>
        </figure>

        <p class="text-justify">En avril 1916, les délégués du Comité International de la Croix-Rouge, dans leur rapport sur leurs visites dans les camps allemands, décrivent dans les termes suivants la peine du peloton avec fardeau, qui, à Chemnitz, a remplacé le poteau: Le prisonnier est emmené sur le terrain d'exécution chargé d'un sac de 15 kilogrammes. Un feldwebel<sup>1</sup> allemand commande un quart d'heure de pas accéléré; un quart d'heure de pas de gymnastique et accéléré; un quart d'heure de pas de gymnastique et de mouvements à genou et couché; un quart d'heure de gymnastique. Entre chaque quart d'heure, le prisonnier à 2 ou 3 minutes de repos. Dans certaines compagnies, un adjudant sous-officier français assiste à l'exécution de la peine.</p><p class="text-justify">Le camp de Chemnitz est évacué le 4 janvier 1919.</p>

        <p class="text-justify" class="note"><sup>1</sup> Le grade de feldwebel se popularise dans l'armée allemande au début du XIXe siècle. Plus haut grade de sous-officier jusqu'en 1918, au sein de la Deutsches Heer (nom de l'armée de terre allemande de 1871 à 1919). Le Feldwebel est alors en charge d'une compagnie. Selon le Larousse, il correspond au grade d'adjudant.</p><p></p>

